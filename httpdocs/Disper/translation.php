function get_language() {
    $ln = $_GET["ln"];
    if (!($ln == "en" || $ln == "de")) {
        $ln = $_COOKIE["ln"];
    } else {
        $ck = $_COOKIE["ln"];
        if ($ck != $ln) {
            setcookie("ln", $ln);
        }
    }

    if (!($ln == "en" || $ln == "de")) {
        $ln = "en";
    }

    return $ln;
}



